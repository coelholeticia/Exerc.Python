algo = input('Digite algo:')
print('O tipo primitivo é:' , type(algo))
print('só tem espaços ? ', algo.isspace())
print('é um numero?', algo.isnumeric())
print('é um alphabeto?', algo.isalpha())
print('é um alphanumerico?', algo.isalnum())
print('é um maiusculo?', algo.isupper())
print('é um minusculo?', algo.islower())
print('é um minusculo?', algo.istitle())

